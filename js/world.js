function drawMap(map) {

  var i = 0;

  for (var y = 0; y < 10; y++) {
    for (var x = 0; x < 10; x++) {
      var tile_x = x * block_w;
      var tile_y = y * block_h;

      var tileType = map[i];

      if (tileType === 0 || tileType === 3) {
        stones.draw(tile_x,tile_y);
      } else if (tileType === 1) {
        back_brown.draw(tile_x,tile_y);
      } else if (tileType === 2) {
        back_brown_bottom.draw(tile_x,tile_y);
      } else if (tileType === 5) {
        yellow.draw(tile_x,tile_y);
      }
      i++;
    }
  }

}
